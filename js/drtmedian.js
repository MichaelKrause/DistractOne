 calculateRtUs : function(subtaskArray){//return in uSec estimated reaction time; input is subtask array e.g. ["2180300", "2190300", "2080300"]
	 //calculation idea: individual medianRT for each subtask is weighted by (meanHitCountSubtask/meanCompleteTaskHitCount)
	 //e.g. a task with two subtask should be calculated. subject3 has 4hits and a medianRT of 600ms for subtask1 and 3hits and a medianRT of 800ms for subtask2:
	 //than the calculation for subject3 is done by (4/7)*600ms + (3/7)*800ms =  343ms + 343ms = 686ms
	 //NOTE filter NaN and '0' from results for further calculation 
 
    if ((!subtaskArray)||(subtaskArray.length==0)){return null;}
 
    //(1) first get hitCountSum for weighting
    var hitCountSum = this.hitCountSum(subtaskArray);
    
    //(2) second step calculate median reaction time and weight by hitCountSum
   var weightedRtSum = [];

    for(var i = 0; i< subtaskArray.length; i++){	
		var subtask = String(subtaskArray[i]);
		var hitCount = this.getHitCount(subtask);
		var trialCount = this['trialCount'][subtask];
		var hitCountPerTrial = math.divide( hitCount, trialCount);
		
	   	if (typeof trialCount != 'undefined'){
		  var mRt = this.getMedianRt(subtask);
		  //weight each reactionTime by hitCountPerTrial/hitCountSum:
		  var weighting = math.dotDivide(hitCountPerTrial,math.transpose(hitCountSum));//weight rt
	
		  var weightedRt = math.dotMultiply(mRt,math.transpose(weighting));//weight mRt
	     if(weightedRtSum.length > 0){
		   weightedRtSum = math.add(weightedRtSum, weightedRt);
		 }else{
		   weightedRtSum = weightedRt;
	     }

	  	}//if defined
	  	
    }//for

  
   return weightedRtSum;
     
  },


