 getSubtaskMean: function(metric, subtask){//getSubtaskMean("tttbase", "2230300"); returns mean if two or three trials are available
 	var trialCount = this['trialCount'][subtask];
   	if (typeof trialCount == 'undefined'){
   	  error('occ.getSubtaskMean() undefined:' +subtask);
	  return null;
  	}else{
	 var temp = this[metric][subtask];
	  if (typeof temp == 'undefined'){
   	    error('occ.getSubtaskMean() undefined:' +subtask);
	    return null;
  	  }else{
	   if (trialCount > 1){
		var temp2 = this[metric][String(parseInt(subtask)+100)];//second trial => code x400
		temp = math.add(temp, temp2); 
	    }
	   if (trialCount > 2){
		var temp3 = this[metric][String(parseInt(subtask)+200)];//third trial => code x500
		temp = math.add(temp, temp3); 
	    }

	   return math.divide( temp, trialCount);//return mean
	  }
	
	}
 },

